#!/usr/bin/python3
'''recursive function to query Reddit API for associated return val'''
import requests


def recurse(subreddit, hot_list=[], count=0):
    '''
    Description:
        - recursive function for querying the Reddit API for a given subreddit
    Return:
        - Success: a list containing the titles of all hot articles
        - Failure: None
    '''

    url = "https://www.reddit.com/r/{}/hot/.json".format(subreddit)
    headers = {
        "User-Agent": "linux:0x16.api.advanced:v1.0.0 (by /u/bdov_)"
    }
    params = {
        "after": after,
        "count": count,
        "limit": 100
    }
    response = requests.get(url, headers=headers, params=params,
                            allow_redirects=False)
    if response.status_code == 404:
        return None

    results = response.json().get("data")
    after = results.get("after")
    count += results.get("dist")
    for c in results.get("children"):
        hot_list.append(c.get("data").get("title"))

    if after is not None:
        return recurse(subreddit, hot_list, after, count)
    return
